class car: #created a class named car and gave it attributes
    num_wheels = 4
    speed = 70
    material = "metal"

class mustang(car): #child car named mustang has its own attributes along with the 'car' attributes
    color = "red"
    doors = 2
    

class civic(car): #child car named civic has its own attributes along with the 'car' attributes
    year = 2009
    transmission = "manual"
    
